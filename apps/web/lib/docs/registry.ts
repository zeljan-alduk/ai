/**
 * Resolve the on-disk path (relative to apps/web/content/docs) for a
 * given page. Generated pages live under `api/_generated/<slug>.md`.
 */
export function pageSourcePath(page: DocPage): string {
  if (page.source) return page.source;
  return `${page.slug}.md`;
}

/**
 * Build the union of static + generated pages. The generator script
 * writes `api/_generated/<endpoint>.json`; pages are listed via
 * `loadGeneratedApiPages()` at request time so adding new endpoints
 * doesn't require rebuilding the registry.
 */
export interface GeneratedApiPage extends DocPage {
  readonly section: 'api';
  readonly generated: true;
}
