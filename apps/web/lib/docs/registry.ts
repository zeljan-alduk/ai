/**
 * Docs registry — single source of truth for the docs site nav + sitemap.
 *
 * Every entry maps a slug (URL segment under `/docs/...`) to:
 *   - the on-disk markdown source under `apps/web/content/docs`
 *   - the section it belongs to in the sidebar
 *   - the human-readable title used in nav, search, and OG images
 *   - an optional `auto` flag (true = generated MDX, do not migrate)
 *
 * The registry is intentionally hand-curated rather than discovered at
 * runtime: it lets us order the sidebar without bolting on filename
 * prefixes (`01-quickstart.md`), and it lets the build-time API-ref
 * generator append rows without renaming files.
 *
 * LLM-agnostic: no doc page is provider-named. The local-models guide
 * does mention runtime adapter identifiers (Ollama, vLLM, llama.cpp,
 * MLX) — those are runtime backends, not LLM providers, and the page
 * is explicitly about them.
 */

export interface DocSection {
  /** URL-safe key, used as the path prefix segment when non-root. */
  readonly key: DocSectionKey;
  /** Sidebar heading. */
  readonly label: string;
  /** True for the implicit "" section that mounts at `/docs` directly. */
  readonly root?: boolean;
}

export type DocSectionKey = 'overview' | 'concepts' | 'guides' | 'api' | 'sdks' | 'reference';

export interface DocPage {
  /** URL slug under /docs (no leading slash, e.g. "concepts/privacy-tier"). */
  readonly slug: string;
  /** Section group in the sidebar. */
  readonly section: DocSectionKey;
  /** Human-readable title; rendered as <h1> and OG image text. */
  readonly title: string;
  /** Short summary, used in the docs landing TOC + search index. */
  readonly summary: string;
  /**
   * Source location relative to `apps/web/content/docs`. When omitted,
   * the slug itself (with a `.md` extension) is assumed.
   */
  readonly source?: string;
  /** True when the file is regenerated by `generate-api-docs.ts`. */
  readonly generated?: boolean;
}

